    public function findProductsByIds(array $productIds)
    {
        return $this->createQueryBuilder('p')
            ->where('p.id in (:items)')
            ->setParameter('items', array_map(function ($id) {
                return Uuid::fromString($id)->toBinary();
            }, $productIds))
            ->getQuery()
            ->getResult();
    }

